// step 4. 연산자
Console.WriteLine("====== === step 4. 연산자 === ======");

sbyte bitA = 64;
Console.WriteLine(bitA << 1);
Console.WriteLine(bitA >> 1);

Console.WriteLine(bitA + 1);
Console.WriteLine(bitA - 1);

sbyte bitB = 10;
Console.WriteLine(bitB++);
Console.WriteLine(bitB);
Console.WriteLine(++bitB);

sbyte bitC = 10;
Console.WriteLine(--bitC);
Console.WriteLine(bitC);
Console.WriteLine(bitC--);


Console.ReadLine();
